# Input:
# set1 = {1, 2, 3, 4}
# set2 = {3, 4, 5, 6}

# Output:
# Union: {1, 2, 3, 4, 5, 6}
# Intersection: {3, 4}

set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

union_set = set1.union(set2)
intersection_set = set1.intersection(set2)

print("Union:", union_set)
print("Intersection:", intersection_set)

# Output:
# Union: {1, 2, 3, 4, 5, 6}
# Intersection: {3, 4}


