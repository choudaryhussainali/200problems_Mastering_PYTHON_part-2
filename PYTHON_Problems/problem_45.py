# Task:
# Generate 5 unique random numbers between 1 and 50 (like a lotto draw).
# Output: [3, 17, 21, 44, 50]
import random

def generate_lotto():
    return random.sample(range(1, 51), 5)

print(generate_lotto())

# [3, 17, 21, 44, 50]


