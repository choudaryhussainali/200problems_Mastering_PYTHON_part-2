# Sort a list of words based on their length (shortest to longest).


# words = ["apple", "kiwi", "banana", "pear"]
# # Output: ['kiwi', 'pear', 'apple', 'banana']

# List of words
words = ["apple", "kiwi", "banana", "fig", "grape"]

# Sort by length (ascending)
sorted_words = sorted(words, key=len)

print(sorted_words)


