# Write a function that converts a binary string to a decimal number.

# Sample Input: "1011"
# Expected Output: 11



def binary_to_decimal(bin_str):
    return int(bin_str, 2)

# Sample Input
print(binary_to_decimal("1011"))

# Output:
# 11


